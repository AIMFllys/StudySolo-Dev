-- _project_registry — 项目注册表
-- =====================================================================
CREATE TABLE IF NOT EXISTS public._project_registry (
  prefix text NOT NULL,
  project_name text NOT NULL,
  description text,
  status text NOT NULL DEFAULT 'active'::text,
  domain text,
  auth_method text,
  repo_url text,
  owner text DEFAULT 'AIMFllys'::text,
  created_at timestamptz DEFAULT now(),
  updated_at timestamptz DEFAULT now(),
  CONSTRAINT _project_registry_pkey PRIMARY KEY (prefix),
  CONSTRAINT _project_registry_status_check CHECK (status = ANY (ARRAY['active','planned','deprecated'])),
  CONSTRAINT _project_registry_auth_method_check CHECK (auth_method = ANY (ARRAY['bcrypt_session','supabase_jwt','none',NULL]))
);
